// Suma
console.log(5+5);

// Resta
console.log(15-5);

// Multiplicacion
console.log(15*5);

// Division
console.log(15/5);

// Modulo
console.log(15%5);

// Incremento
let numero = 10;
console.log(++numero);
console.log(numero++);

// Decremento
let num = 10;
console.log(num--);
console.log(num);

// Comparaciones - simple 
console.log(5 == '5'); // true

// Comparaciones - estricta
console.log(5 === '5'); // false

// Desigualdad  - simple 
console.log(5 != '5'); // false

// Desigualdad  - estricta
console.log(5 !== '5'); // true

// >
console.log(5 > 3); // true

// >=
console.log(5 >= 5); // true

// <
console.log(5 < 5); // false

// <=
console.log(5 <= 5); // true

// AND &&
console.log (false && true); //false
console.log (true && true); //true

// OR ||
console.log (false || true); //true
console.log (true || true); //true
console.log (false || false); //false

// concatenacion
let nombre = "Carlos";
let apellido = "Velasquez";
console.log(nombre +" "+ apellido);


